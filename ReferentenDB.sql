CREATE TABLE Team (

    Team_ID INTEGER auto_increment,
    Team_Name TEXT NOT NULL,
    PRIMARY KEY (Team_ID)

);

CREATE TABLE Bearbeiter (

    Bearbeiter_ID INTEGER auto_increment,
    Bearbeiter_NachName TEXT NOT NULL,
    Bearbeiter_VorName TEXT NOT NULL,
    PRIMARY KEY (Bearbeiter_ID)

);

CREATE TABLE Rolle (

    Rolle_ID INTEGER  auto_increment,
    Rolle_Bearbeiter_ID INTEGER,
    Rolle_Zugehoerigkeit_Team_ID INTEGER,
    istPlaner_C1 BOOL DEFAULT 0,
    istPlaner_C2 BOOL DEFAULT 0,
    istPlaner_C2_Ü BOOL DEFAULT 0,
    istBbgl BOOL DEFAULT 0,
    PRIMARY KEY (Rolle_ID),
    FOREIGN KEY (Rolle_Bearbeiter_ID) REFERENCES Bearbeiter(Bearbeiter_ID),
    FOREIGN KEY (Rolle_Zugehoerigkeit_Team_ID) REFERENCES Team(Team_ID)

);

CREATE TABLE Bereich (

    Bereich_ID INTEGER auto_increment,
    Bereich_Name TEXT NOT NULL,
    Bereich_Zugehoerigkeit_Team_ID INTEGER,
    PRIMARY KEY (Bereich_ID),
    FOREIGN KEY (Bereich_Zugehoerigkeit_Team_ID) REFERENCES team(Team_ID)

);

CREATE TABLE Be_Bbgl (

    Be_Bbgl_ID INTEGER auto_increment,
    Be_Bbgl_Bereich_ID INTEGER,
    Be_Bbgl_Bbgl_ID INTEGER,
    PRIMARY KEY (Be_Bbgl_ID),
    FOREIGN KEY (Be_Bbgl_Bereich_ID) REFERENCES Bereich(Bereich_ID),
    FOREIGN KEY (Be_Bbgl_Bbgl_ID) REFERENCES Rolle(Rolle_ID)

);

